                      function updateClock() {
                        var now = new Date();
                        var days = ['Minggu', 'Senin', 'Selasa', 'Rabu', 'Kamis', 'Jumat', 'Sabtu'];
                        var day = days[now.getDay()];
                        var date = now.getDate();
                        var month = now.getMonth() + 1;
                        var year = now.getFullYear();
                        var hours = now.getHours();
                        var minutes = now.getMinutes();
                        var seconds = now.getSeconds();

                        // Format waktu dan tanggal
                        var formattedDate = day + ', ' + '<br>' + addLeadingZero(date) + ' ' + getMonthName(month) + ' ' + year;
                        var formattedTime = addLeadingZero(hours) + '.' + addLeadingZero(minutes) + '.' + addLeadingZero(seconds);

                        document.getElementById('clock').innerHTML = formattedDate + '<br>' + formattedTime;

                      }

                      function addLeadingZero(number) {
                        return (number < 10 ? '0' : '') + number;
                      }

                      function getMonthName(month) {
                        var months = [
                          'Januari', 'Februari', 'Maret', 'April',
                          'Mei', 'Juni', 'Juli', 'Agustus',
                          'September', 'Oktober', 'November', 'Desember'
                        ];
                        return months[month - 1];
                      }

                      setInterval(updateClock, 1000);
